int		ft_sqrt(int nb)
{
	int i;
	int res;

	i = 0;
	res = 0;
	if (nb == 4)
		return (2);
	if (nb == 9)
		return (3);
	while (i <= nb / 4)
	{
		if ((i * i) == nb)
		{
			res = i;
			break ;
		}
		i++	;
	}
	return (res);
}
