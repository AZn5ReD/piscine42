int		ft_max(int *tab, int length)
{
	int i;
	int tmp;

	i = 0;
	while (i < length)
	{
		if (tab[i] > tab[i + 1])
		{
			tmp = tab[i];
			tab[i] = tab[i + 1];
			tab[i + 1] = tmp;
			i = 0;
		}
		else
			i++;
	}
	return (tab[length - 1]);
}
