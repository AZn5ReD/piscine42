int		ft_antidote(int i, int j, int k)
{
	int l;
	int tmp;
	int tab[3];

	tab[0] = i;
	tab[1] = j;
	tab[2] = k;
	l = 0;
	while (l < 2)
	{
		if (tab[l] > tab[l + 1])
		{
			tmp = tab[l];
			tab[l] = tab[l + 1];
			tab[l + 1] = tmp;
			l = 0;
		}
		else
			l++;
	}
	return (tab[1]);
}
