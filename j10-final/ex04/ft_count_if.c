int		ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int ret;

	i = 0;
	ret = 0;
	while (tab[i] != 0)
	{
		if (f(tab[i]) == 1)
			ret += 1;
		i++;
	}
	return (ret);
}
