int		ft_sudoku_checker(int **sudoku, int **sudoku2)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (i < 9)
	{
		while (j < 9)
		{
			if (sudoku2[i][j] != sudoku[i][j])
				return (0);
			j++;
		}
		i++;
		j = 0;
	}
	return (1);
}
