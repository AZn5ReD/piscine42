#include <unistd.h>

int		ft_17check(int **sudoku)
{
	int i;
	int j;
	int c;

	i = 0;
	j = 0;
	c = 0;
	while (i < 9)
	{
		j = 0;
		while (j < 9)
		{
			if (sudoku[i][j])
				c = c + 1;
			j++;
		}
		i++;
	}
	if (c > 16)
		return (0);
	write(1, "Error/n", 6);
	return (1);
}
